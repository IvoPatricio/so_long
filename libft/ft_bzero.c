#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*dest;

	dest = (char *)s;
	if (n == 0)
		return ;
	i = 0;
	while (i < n)
	{
		dest[i] = 0;
		i++;
	}
	s = dest;
}
